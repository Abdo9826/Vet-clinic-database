CREATE TABLE animals (
    id int GENERATED BY DEFAULT AS IDENTITY,
    name varchar(100),
    date_of_birth date,
    escape_attempts int,
    neutered boolean,
    weight_kg decimal
);

Alter TABLE animals ADD species char(20);


CREATE TABLE owners (
    id int GENERATED BY DEFAULT AS IDENTITY,
    full_name varchar(100),
    age int,
    PRIMARY KEY (id)
);


CREATE TABLE species (
    species_id int GENERATED BY DEFAULT AS IDENTITY,
     name varchar(100),
    age int,
    PRIMARY KEY (species_id)
);


